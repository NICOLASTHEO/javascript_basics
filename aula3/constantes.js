// a keyword LET permite que alterarmos o valor da variável.
let valorIngressoAdulto = 20;
valorIngressoAdulto = 30;
console.log(valorIngressoAdulto)


// a keyword CONST torna a variável estática, não podendo mais ser alterada.
const valorIngressoAdolescente = 20;
// valorIngressoAdolescente = 30; dará erro pois não podem haver DOIS valores diferentes para a mesma variável.
console.log(valorIngressoAdolescente)